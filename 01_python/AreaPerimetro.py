print("Vamos a calcular el permietro y area de un cuadrado")
lado = int(input("Introduce el lado: "))
perimetro = lado * 4
area = lado ** 2
print("Perimetro: ", perimetro, " Área: ", area)

print("Vamos a calcular el perimetro y área de un cubo")
lado = int(input("Introduce el lado: "))
perimetro = lado * 6
area = (lado ** 2) * 6
print("Perimetro: ", perimetro, " Área: ", area)
